export function Soma(n1: number, n2: number): number {
  return n1 + n2;
}

export function Subtracao(n1: number, n2: number): number {
  return n1 - n2;
}

export function Multiplicacao(n1: number, n2: number): number {
  return n1 * n2;
}

export function Divisao(n1: number, n2: number): number {
  return n1 / n2;
}
